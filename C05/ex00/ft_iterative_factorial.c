int	ft_iterative_factorial(int nb)
{
	unsigned int	f;
	int				i;

	if (nb < 0)
		return (0);
	f = 1;
	i = 1;
	while (i <= nb)
	{
		f = f * i;
		++i;
	}
	return (f);
}
