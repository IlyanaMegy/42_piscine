int	ft_is_prime(int nb)
{
	int	c;
	int	i;

	i = 1;
	c = 0;
	if (nb <= 1)
		return (0);
	while (i <= nb)
	{
		if (nb % i == 0)
			++c;
		++i;
	}
	if (c > 2)
		return (0);
	return (1);
}
