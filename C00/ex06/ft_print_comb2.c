#include <unistd.h>

void	show_me(char c)
{
	write(1, &c, 1);
}

void	ft_print_comb2(void)
{
	int		a;
	int		b;

	a = 0;
	while (a <= 100)
	{
		b = a;
		while (b <= 99)
		{
			if (a != b)
			{
				show_me(a / 10 + '0');
				show_me(a % 10 + '0');
				write(1, " ", 1);
				show_me(b / 10 + '0');
				show_me(b % 10 + '0');
				if (a != 98)
					write(1, ", ", 2);
			}
			b++;
		}
		a++;
	}
}
