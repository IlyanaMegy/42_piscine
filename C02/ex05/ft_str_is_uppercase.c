int	ft_str_is_uppercase(char *str)
{
	int	i;
	int	c;

	i = 0;
	while (str[i])
	{
		c = str[i];
		if (!(c >= 65 && c <= 90))
			return (0);
		i++;
	}
	return (1);
}
